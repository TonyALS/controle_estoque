    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        try {
            //Receber a data de pesquisa:
            DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDate dataVenda = LocalDate.parse(txtData.getText(), formato);
            
            double totalVenda = 0;
            VendasDAO dao = new VendasDAO();
            totalVenda = dao.retornaTotalVendaPorData(dataVenda);
            
            txtTotalVenda.setText(String.valueOf(totalVenda));
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Digite uma data v√°lida.");
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

